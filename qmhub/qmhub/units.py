"""Unit conversion and physical constants.
Internal units:
    Energy: kcal/mol
    Length: angstrom (A)
Atomic units:
    Energy: hartree (Eh)
    Length: bohr (a0)
"""

# Unit ratios
KCAL_TO_JOULE = 4.184e+03
ANGSTROM_TO_METER = 1e-10

# 2008 CODATA
CODATA08_AVOGADRO_CONSTANT = 6.0221417930e+23
CODATA08_HARTREE_TO_JOULE = 4.3597439422e-18
CODATA08_EV_TO_JOULE = 1.60217648740e-19
CODATA08_BOHR_TO_METER = 0.5291772085936e-10

CODATA08_HARTREE_TO_KCAL = CODATA08_HARTREE_TO_JOULE / KCAL_TO_JOULE * CODATA08_AVOGADRO_CONSTANT
CODATA08_BOHR_TO_A = CODATA08_BOHR_TO_METER / ANGSTROM_TO_METER
CODATA08_HARTREE_TO_EV = CODATA08_HARTREE_TO_JOULE / CODATA08_EV_TO_JOULE

# AmberTools Constants
AMBER_HARTREE_TO_EV = 27.21
AMBER_EV_TO_KCAL= 23.061
AMBER_HARTREE_TO_KCAL = AMBER_HARTREE_TO_EV * AMBER_EV_TO_KCAL
AMBER_BOHR_TO_A = 0.529177249

# ORCA
ORCA_BOHR_TO_A = 0.5291772083
